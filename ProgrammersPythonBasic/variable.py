# 변수 : 어떤 값을 다음 사용시 참조하기 쉽도록 정의하는 것(이름을 붙여주는 것)

print('안녕! 나는 은경이야. 30살이야')
print('안녕! 나는 ','은경', '이야. ', 30 ,'살이야')

# identity와 age는 변수
identity = '은경'
age = 30
print('안녕! 나는 ', identity , '이야. ', age ,'살이야')

# 변수에 새로운 값을 입력하는 방법은 변수를 선언하는 것과 같다

identity = '개미'
print('안녕! 나는 ', identity , '이야. ', age ,'살이야')
